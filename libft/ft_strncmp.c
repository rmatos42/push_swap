#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*us1;
	unsigned char	*us2;
	size_t			i;

	us1 = (unsigned char *)s1;
	us2 = (unsigned char *)s2;
	i = 0;
	while ((*us1 || *us2) && i < n)
	{
		if (*us1 != *us2)
			return (*us1 - *us2);
		us1++;
		us2++;
		i++;
	}
	return (0);
}
